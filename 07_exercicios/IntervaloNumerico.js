const texto = '0 1 10 192 199 201 249 255 256 312 1010 1512';

// Números entre 0-255:

// \d{1,2} de 0 até 99.
// 1\d{2} de 100 até 199.
// 2[0-4]\d de 200 até 249.
// 25[0-5] de 250 até 255.

// [OUT]:
// [ '0', '1', '10', '192', '199', '201', '249', '255' ]
console.log(texto.match(/\b(\d{1,2}|1\d{2}|2[0-4]\d|25[0-5])\b/g));
